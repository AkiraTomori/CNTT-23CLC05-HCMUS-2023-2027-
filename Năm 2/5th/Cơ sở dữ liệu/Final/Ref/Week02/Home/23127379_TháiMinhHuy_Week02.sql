-- Tạo lược đồ quan hệ và khai báo khóa chính

CREATE TABLE GIAOVIEN
(
    MAGV VARCHAR(10) NOT NULL,
    HOTEN NVARCHAR(20),
    LUONG INT,
    PHAI NVARCHAR(5),
    NGSINH DATE,
    DIACHI NVARCHAR(50),
    GVQLCM VARCHAR(10),
    MABM NVARCHAR(10),
    PRIMARY KEY (MAGV)
)

CREATE TABLE KHOA
(
    MAKHOA VARCHAR(10) NOT NULL,
    TENKHOA NVARCHAR(20),
    NAMTL INT,
    PHONG VARCHAR(5),
    DIENTHOAI VARCHAR(15),
    TRUONGKHOA VARCHAR(10),
    NGAYNHANCHUC DATE,
    PRIMARY KEY (MAKHOA)
)

CREATE TABLE BOMON
(
    MABM NVARCHAR(10) NOT NULL,
    TENBM NVARCHAR(20),
    PHONG NVARCHAR(10),
    DIENTHOAI VARCHAR(15),
    TRUONGBM VARCHAR(10),
    MAKHOA VARCHAR(10),
    NGAYNHANCHUC DATE,
    PRIMARY KEY (MABM)
)

CREATE TABLE GV_DT
(
    MAGV VARCHAR(10) NOT NULL,
    DIENTHOAI VARCHAR(15) NOT NULL,
    PRIMARY KEY (MAGV, DIENTHOAI)
)

CREATE TABLE NGUOITHAN
(
    MAGV VARCHAR(10) NOT NULL,
    TEN NVARCHAR(20) NOT NULL,
    NGSINH DATE,
    PHAI NVARCHAR(5),
    PRIMARY KEY (MAGV, TEN)
)

CREATE TABLE CHUDE
(
    MACD NVARCHAR(10) NOT NULL,
    TENCD NVARCHAR(30),
    PRIMARY KEY (MACD)
)

CREATE TABLE DETAI
(
    MADT NVARCHAR(10) NOT NULL,
    TENDT NVARCHAR(50),
    CAPQL NVARCHAR(15),
    KINHPHI INT,
    NGAYBD DATE,
    NGAYKT DATE,
    MACD NVARCHAR(10),
    GVCNDT VARCHAR(10),
    PRIMARY KEY (MADT)
)

CREATE TABLE CONGVIEC
(
    MADT NVARCHAR(10) NOT NULL,
    STT INT NOT NULL,
    TENCV NVARCHAR(50),
    NGAYBD DATE,
    NGAYKT DATE,
    PRIMARY KEY(MADT, STT)
)

CREATE TABLE THAMGIADT
(
    MAGV VARCHAR(10) NOT NULL,
    MADT NVARCHAR(10) NOT NULL,
    STT INT NOT NULL,
    PHUCAP FLOAT,
    KETQUA NVARCHAR(10),
    PRIMARY KEY(MAGV, MADT, STT)
)

